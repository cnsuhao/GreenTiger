void C指定路径特征码查杀Dlg::OnBnClickedButtonselect()
{
	// TODO: 在此添加控件通知处理程序代码
	WCHAR szPath[MAX_PATH];     //存放选择的目录路径 
	CString str;

	ZeroMemory(szPath, sizeof(szPath));   

	BROWSEINFO bi;   
	bi.hwndOwner = m_hWnd;   
	bi.pidlRoot = NULL;   
	bi.pszDisplayName = szPath;   
	bi.lpszTitle = L"请选择需要扫描的目录：";   
	bi.ulFlags = 0;   
	bi.lpfn = NULL;   
	bi.lParam = 0;   
	bi.iImage = 0;   
	//弹出选择目录对话框
	LPITEMIDLIST lp = SHBrowseForFolder(&bi); 

	//将一个条目的标识符列表转换为文件系统路径。
	if(lp && SHGetPathFromIDList(lp, szPath))	
	{	
		m_Path = szPath;
		UpdateData(FALSE);
	}else if (bi.iImage!=0)		//如果是点击取消按钮
	{
		AfxMessageBox(L"无效的目录，请重新选择"); 
	}
}

